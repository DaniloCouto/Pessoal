package br.com.fulltime.aprendiz.databasedemesa.classes;

public class Atributos {
	protected float con;
	protected float forc;
	protected float dex;
	protected float agi;
	protected float inte;
	protected float sab;
	
	public float getCon() {
		return con;
	}
	public void setCon(float con) {
		this.con = con;
	}
	public float getForc() {
		return forc;
	}
	public void setForc(float forc) {
		this.forc = forc;
	}
	public float getDex() {
		return dex;
	}
	public void setDex(float dex) {
		this.dex = dex;
	}
	public float getAgi() {
		return agi;
	}
	public void setAgi(float agi) {
		this.agi = agi;
	}
	public float getInte() {
		return inte;
	}
	public void setInte(float inte) {
		this.inte = inte;
	}
	public float getSab() {
		return sab;
	}
	public void setSab(float sab) {
		this.sab = sab;
	}
	
	
}
